package com.lq186.oauth2.handler;

import com.fasterxml.jackson.databind.ObjectMapper;
import com.lq186.oauth2.error.ExceptionResultBeanProcessor;
import org.springframework.http.HttpStatus;
import org.springframework.security.access.AccessDeniedException;
import org.springframework.security.web.access.AccessDeniedHandler;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

public class ResultBeanAccessDeniedHandler implements AccessDeniedHandler {

    private final ObjectMapper objectMapper;

    public ResultBeanAccessDeniedHandler(ObjectMapper objectMapper) {
        this.objectMapper = objectMapper;
    }

    @Override
    public void handle(HttpServletRequest request, HttpServletResponse response, AccessDeniedException accessDeniedException) throws IOException, ServletException {
        response.setStatus(HttpStatus.FORBIDDEN.value());
        ExceptionResultBeanProcessor.process(objectMapper, request, response, accessDeniedException);
    }

}
